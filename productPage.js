var cartProducts=JSON.parse(localStorage.getItem("cartProducts")) || [];
function showProducts(allProducts) {
  allProducts.forEach(function (item) {
    var box = document.createElement("div");

    var img = document.createElement("img");
    img.setAttribute("src", item.image);

    var name = document.createElement("p");
    name.innerText = item.name;
    var price = document.createElement("h4");
    price.innerText = "₹ " + item.price;

    var checkout = document.createElement("button");
    checkout.style.border = "1px";
    checkout.style.backgroundColor = "white";

    checkout.addEventListener("click",function(){
        cartProducts.push(item);
        localStorage.setItem("cartProducts",JSON.stringify(cartProducts))
    })

    box.addEventListener("click",function(){
     localStorage.setItem("cartItem",JSON.stringify(item))
      window.location.href="checkout.html"
    })


    box.addEventListener("mouseover", function () {
      checkout.style.border = "1px solid";
      checkout.innerText = "Add to Basket";
      checkout.style.backgroundColor = "#FAA619";
      checkout.style.border = "1px solid #FAA619";
      checkout.style.color = "white";
      checkout.style.display = "block";
    });

    box.addEventListener("mouseleave", function () {
      checkout.style.display = "none";
    });

    box.append(img, price, name, checkout);
    document.querySelector("#productContainer").append(box);
  });
}

